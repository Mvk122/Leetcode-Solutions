struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

class Solution {
public:
    void traverser(TreeNode* node, vector<int> &result) {
        if (node) {
            this->traverser(node->left, result);
            result.push_back(node->val);
            this->traverser(node->right, result);
        }


    }   
    vector<int> inorderTraversal(TreeNode* root) {
        vector<int> result;
        this->traverser(root, result);
        return result;

    }
};